package br.com.proj.entidade;

public class Moto {
	
	private int moto;
	private String descricao;
	private int ano;
	public int getMoto() {
		return moto;
	}
	public void setMoto(int moto) {
		this.moto = moto;
	}
	public String getDescricao() {
		return descricao;
	}
	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}
	public int getAno() {
		return ano;
	}
	public void setAno(int ano) {
		this.ano = ano;
	}
	
	
	
	

}
